# You have the number 2 as a variable. Multiply it by 2 in two ways.
# Accordingly, you need to divide it in 2 different ways by 2.

number = 2

print(number * 2)
print(number ** 2)
print(int(number / 0.5))

print(int(number / 2))
print(number // 2)

number *= 2  # number = number * 2, 3й способ умножения, чтоб не использовать дробь и деление

print(number)
